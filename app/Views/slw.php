            <h2>PC/Laptop freezes, slow, loads a lot, etc.</h2>
            <p>Please check if your storage is almost full (+90%). Open file explorer and check the C: drive. If so, then follow the steps:<br><br>
        </div>


        <h3>First Solution</h3>
        
            1. Remove the softwares/applications that you don't need at the moment, too much applications running at the same time can lead to slower device.<br><br>
    

        <h3>Second Solution</h3>
            1. Press <b>windows + R</b> to open windows run. <br>
            <img src="img/win+R.png" alt=""><br><br><br>
            2. Type <b>%temp%</b> and enter.<br>
            <img src="img/temp.png" alt=""><br><br><br>
            3. Press <b>windows + a</b> to highlight all the files.<br>
            4. Press <b>shift + delete</b> to permanently delete all temporary files.<br>
            <img src="img/del.png" alt="" width="30%" height="30%"><br><br><br>
            5. If ever this pops up, just tick the check box   <i><b>"Do this for all current items"</b></i> and click on skip on all files.<br>
            <img src="img/check.png" alt=""><br><br>

        <h3>Third Solution</h3><br>
        Lastly, restart your device and be sure to save everything first (doc, xlsx, ppt, etc.).
